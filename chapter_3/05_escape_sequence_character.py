a = "i am the best\nno one is best than me" #\n is an escape sequence character which means new line
print(a)

b = "this is my life i am\thappy" #\t is an escape sequence character which means tab space
print(b)

C = "I AM IN ROOM \"523\"" #\" is an escape sequence character which means double quote
print(C)

D = "CALL ME ON \\ 1234567890" #\\ is an escape sequence character which means backslash
print(D)